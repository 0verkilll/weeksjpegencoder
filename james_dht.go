// writeJamesDHT writes a DHT marker matching f5.jar's james.JpegEncoder.
//
// This function replicates the DHT writing behavior from JpegEncoder.java:
//  1. Tables are written in order: DC_LUM, AC_LUM, DC_CHROM, AC_CHROM.
//  2. Each table's header byte is taken from bits[0], which holds the proper
//     ITU-T T.81 Tc/Th class-ID nibble (0x00, 0x10, 0x01, 0x11).
//
// This is necessary for byte-identical output with f5.jar.
//
//goland:noinspection GoUnusedFunction
func writeJamesDHT(w io.Writer) error {
	tables := getJamesHuffmanTables()

	// Calculate total length
	// Length = 2 + sum(1 + 16 + len(values)) for each table
	// In James format: 1 byte header (bits[0]) + 16 bytes (bits[1..16]) + values
	length := 2
	for _, table := range tables {
		length += 17 + len(table.values) // 1 header + 16 counts + values
	}

	// DHT marker (0xFF 0xC4)
	if _, err := w.Write([]byte{0xFF, 0xC4}); err != nil {
		return err
	}

	// Length (validated to be within uint16 range by DHT table structure)
	// #nosec G115 - DHT length is bounded by table structure (max ~432 bytes for all 4 tables)
	if err := binary.Write(w, binary.BigEndian, uint16(length)); err != nil {
		return err
	}

	// Write each table in James format
	for _, table := range tables {
		// Write header byte (bits[0] = Tc/Th class-ID nibble)
		if _, err := w.Write([]byte{byte(table.bits[0])}); err != nil {
			return err
		}

		// Write counts for each code length (bits[1..16])
		for i := 1; i <= 16; i++ {
			if _, err := w.Write([]byte{byte(table.bits[i])}); err != nil {
				return err
			}
		}

		// Write values
		if _, err := w.Write(table.values); err != nil {
			return err
		}
	}

	return nil
}

// GetJamesStyleHuffmanSpecs returns Huffman table specs in James Weeks order
// but with correct class/ID values for use with standard decoders.
//
// The order is: DC_LUM, AC_LUM, DC_CHROM, AC_CHROM
// This matches the order written by writeJamesDHT.
//
//goland:noinspection GoUnusedExportedFunction
func GetJamesStyleHuffmanSpecs() []jpeg.HuffmanSpec {
	return []jpeg.HuffmanSpec{
		{Class: 0, ID: 0, Bits: jpeg.StdEncoderDCLuminanceBits, Values: copyByteSlice(jpeg.StdEncoderDCLuminanceValues)},
		{Class: 1, ID: 0, Bits: jpeg.StdEncoderACLuminanceBits, Values: copyByteSlice(jpeg.StdEncoderACLuminanceValues)},
		{Class: 0, ID: 1, Bits: jpeg.StdEncoderDCChrominanceBits, Values: copyByteSlice(jpeg.StdEncoderDCChrominanceValues)},
		{Class: 1, ID: 1, Bits: jpeg.StdEncoderACChrominanceBits, Values: copyByteSlice(jpeg.StdEncoderACChrominanceValues)},
	}
}

// copyByteSlice creates a copy of a byte slice.
func copyByteSlice(src []byte) []byte {
	dst := make([]byte, len(src))
	copy(dst, src)
	return dst
}
