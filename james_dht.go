// Package weeksjpegencoder provides f5.jar-compatible JPEG encoding.
//
// This file implements the DHT (Define Huffman Table) marker writer matching
// f5.jar's james.Huffman exactly. The tables are emitted in James order
// (DC_LUM, AC_LUM, DC_CHROM, AC_CHROM); the per-table header byte carries the
// standard ITU-T T.81 Tc/Th class-ID nibble (0x00, 0x10, 0x01, 0x11) pulled
// from bits[0] of each table literal.

package weeksjpegencoder

import (
	"encoding/binary"
	"io"

	"github.com/0verkilll/jpeg"
)

// =============================================================================
// James R. Weeks DHT Tables
// =============================================================================

// jamesHuffmanTable represents one Huffman table in f5.jar's wire layout.
// f5.jar stores 17 ints per table in `bits`: bits[0] is the Tc/Th class-ID
// byte written into the DHT segment, and bits[1..16] are the standard BITS
// counts. We preserve that 17-element shape to make the porting obvious.
type jamesHuffmanTable struct {
	// bits[0] = Tc/Th class-ID byte (e.g. 0x00 for DC luma, 0x10 for AC luma);
	// bits[1..16] = count of codes per length (1..16 bits).
	bits [17]int
	// values contains the symbol values in order of increasing code length.
	values []byte
}

